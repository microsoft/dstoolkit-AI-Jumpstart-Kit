using Microsoft.SemanticKernel;
using System.ComponentModel;

namespace SK_Planner.Plugins;

public class EmailPlugin
{
    [KernelFunction]
    [Description("Sends an email to a recipient.")]
    public async Task SendEmailAsync(
        Kernel kernel,
        [Description("Semicolon delimitated list of emails of the recipients")] string recipientEmails,
        string subject,
        string body
    )
    {
        Console.WriteLine("*************SendEmailAsync************");

        Console.WriteLine($"\nRec:{recipientEmails}  \nSub:{subject}  \nBody:{body}  \nEmail sent!");
    }
}